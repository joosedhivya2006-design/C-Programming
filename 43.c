//43.Greater than or equal to  or Less than equal to 

//>=
#include<stdio.h>
int main(){
	int a,b;
	Scanf("%d",&a);
	scanf("%d",&b);
	printf("%d",a>=b);
	return 0;
}


//<=
#include<stdio.h>
int main(){
	int a,b;
	Scanf("%d",&a);
	scanf("%d",&b);
	printf("%d",a<=b);
	return 0;
}
